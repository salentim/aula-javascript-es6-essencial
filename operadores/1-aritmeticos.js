// RESTO DIVISÃO %

12 % 5 // retorna 2

// INCREMENTO ++

const a = ++2 // começa a partir do 3 
const b = 2++ // começa a partir do 2

// DECREMENTO --

const c = --2 // começa a partir do 1
const d = 2-- // começa a partir do 2

// NEGAÇÃO -

-3
-true   // retorna -1

// ADIÇÃO

+"3"    // retorna 3
+true   // retorna 1
+false  // retorna 0

// MULTIPLICAÇÃO

5 * 5   // retorna 25
3 * 4   // retorna 12

// EXPONENCIAÇÃO **

2 ** 3      // retorna 8
10 ** -1    // retorna 0.1

// AGRUPAMENTO

2 * (3 + 4)